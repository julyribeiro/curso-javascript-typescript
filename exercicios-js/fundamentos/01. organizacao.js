// Organização básica de um código JavaScript

// 1. Sentença de código: É a unidade básica (pode ou não terminar com ponto e vírgula).
console.log("Linha/Sentença de código");

// 2. Blocos de código: Servem para agrupar sentenças de código. 
// São delimitados por chaves { }.
// Um bloco pode estar dentro de outro (aninhamento), criando diferentes níveis de escopo.
{
    console.log("Início de um bloco de código");
    {
        console.log("Bloco de código aninhado (interno)");
    }
    console.log("Fim do bloco externo");
}

// O JavaScript é organizado em sentenças que podem ser agrupadas em blocos, 
// que por sua vez compõem o arquivo do programa.