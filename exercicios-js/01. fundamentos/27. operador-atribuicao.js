// Operadores de Atribuição (Shorthands)
const a = 7;
let b = 3;

b += a; // Atribuição Aditiva: b = b + a (Resulta em 10)
console.log(b);

b -= 4; // Atribuição Subtrativa: b = b - 4 (Resulta em 6)
console.log(b);

b *= 2; // Atribuição Multiplicativa: b = b * 2 (Resulta em 12)
console.log(b);

b /= 2; // Atribuição Divisiva: b = b / 2 (Resulta em 6)
console.log(b);

b %= 2; // Atribuição Modular: b = b % 2 (Resto da divisão. Se par = 0, se ímpar = 1)
console.log(b);