// Em JavaScript, a Função é a peça central da linguagem.
// "Se o JS tivesse um presidente, seria a Função."

console.log(typeof Object); // Object em JS é uma função!

// Qualquer classe em JS também é, por baixo dos panos, uma função.
class Produto {}
console.log(typeof Produto); // function

/*
   Conclusão: 
   Quase tudo no JavaScript deriva de uma função. 
   Entender funções é a chave para dominar a linguagem.
*/