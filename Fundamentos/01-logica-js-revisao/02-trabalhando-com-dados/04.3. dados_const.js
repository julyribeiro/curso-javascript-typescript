// LET: Permite reatribuição (mudar o valor da variável).
let a = 3;
a = a + 7; // Modificação do valor
console.log(a); // Resultado: 10

// CONST: Define uma constante. O valor NÃO pode ser alterado após a criação.
const b = 10;
// b = 15; // Erro! Não se pode atribuir novo valor a uma constante.

// DICA: Use 'const' por padrão para deixar o código mais seguro. 
// Use 'let' apenas quando souber que o valor precisará mudar.