// ## ESTRUTURA DE DADOS
// É a forma como organizamos e administramos os dados para que possam ser usados de forma eficiente.
// "Estrutura de dados é diferente de apenas Dados isolados".

// Exemplos de estruturas comuns:
// - Lista: Itens ordenados.
// - Árvore: Estrutura hierárquica.
// - Fila: Segue uma ordem específica de entrada e saída.

// ## Conceitos de Organização (Fluxo de Dados)
// * FIFO (First In, First Out): O primeiro a entrar é o primeiro a sair (ex: fila de banco).
// * LIFO (Last In, First Out): O último a entrar é o primeiro a sair (ex: pilha de pratos).

// ## Tipos e Armazenamento
// 1. Estruturas Primárias (Tipos Primitivos): Inteiro, Real (Float), Letra (Char), Booleano.
// 2. Variáveis e Constantes: Permitem armazenar dados na memória rotulando o espaço (endereço).
//    - Variável: O valor pode ser alterado durante a execução.
//    - Constante: O valor é fixo e não permite alteração.

// ## Operações Fundamentais
// São as ferramentas que usamos para manipular as estruturas:
// - Atribuição: Colocar um valor dentro de uma variável ( = ).
// - Aritméticos: Cálculos matemáticos ( +, -, *, / ).
// - Relacionais: Comparações ( >, <, ==, != ).
// - Lógicas: Combinação de condições ( &&, ||, ! ).

// ## Estruturas Personalizadas / Objetos
// Quando os tipos primários não bastam, podemos mapear entidades do mundo real.
// Exemplo: Criar uma estrutura "Cliente" que agrupa Nome, CPF e Telefone.