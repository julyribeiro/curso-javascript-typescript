// DESAFIO: Cálculo da área de uma circunferência.
// Fórmula: PI * raio * raio (ou PI * raio²)

const PI = 3.141592; 
let raio = 10;
let areaCircunferencia = PI * raio * raio;

console.log("O valor da área é: " + areaCircunferencia + " m².");

