// Bloco de Código: Conjunto de instruções agrupadas, tratadas como uma unidade.
// Definição em JS: É delimitado por um par de chaves { ... }.

{
    console.log("Sentença dentro de um bloco");
    console.log("Outra sentença do mesmo bloco");
}

// Blocos Aninhados: É possível criar blocos dentro de outros blocos.
{
    console.log("Início do bloco pai");
    {
        console.log("Bloco filho (nível 2)");
        {
            console.log("Bloco neto (nível 3)");
        }
    }
    console.log("Fim do bloco pai");
}
// Observação: Blocos são fundamentais para definir o escopo de variáveis e estruturas de controle.