// * Sentença de Código: É a unidade básica de um programa, semelhante a uma frase em um texto. 
// * Analogia: Letras formam palavras, que formam frases. No computador, sentenças formam blocos.
// * Organização: Um programa é organizado em pastas e arquivos contendo essas sentenças.

// * Comprimento: Uma sentença pode ocupar várias linhas ou ser bem curta.
// * Ponto e vírgula (;): Em JS é opcional na maioria dos casos, ao contrário de linguagens como Java ou C#.
// * DEFINIÇÃO: Sentenças são instruções para o computador realizar ações (fazer isso ou aquilo).

console.log("Essa é uma sentença de código"); 

// Exemplo com 3 sentenças de código:
console.log("Teste 1");
console.log("Teste 2");
console.log("Teste 3");

// Exemplo de 3 sentenças na mesma linha (Não recomendado por dificultar a leitura):
console.log("Teste 1"); console.log("Teste 2"); console.log("Teste 3");